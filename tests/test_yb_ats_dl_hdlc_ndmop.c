/* ── N4: UI frame rejected in NDM ──────────────────────────────────────── */

static void test_ndmop_ui_rejected_in_ndm(void **state) {
	(void)state;
	mock_transport_pair_t pair;
	mock_transport_pair_init(&pair);

	osp_hdlc_session_t server;
	osp_hdlc_session_init_server(&server, &pair.server_transport, 1, 1, 1, 1);

	assert_int_equal(server.state, OSP_HDLC_STATE_IDLE);

	/* Build a UI frame */
	osp_hdlc_frame_t ui;
	memset(&ui, 0, sizeof(ui));
	osp_hdlc_address_init(&ui.destination, 1, 1);
	osp_hdlc_address_init(&ui.source, 1, 1);
	ui.control.type = OSP_HDLC_TYPE_UI;
	ui.control.poll_final = true;

	uint8_t ui_info[] = {0xE6, 0xE6, 0x00, 0x60};
	memcpy(ui.info, ui_info, sizeof(ui_info));
	ui.info_len = sizeof(ui_info);

	uint8_t ui_enc[128];
	uint32_t ui_len = 0;
	assert_int_equal(osp_hdlc_frame(&ui, ui_enc, sizeof(ui_enc), &ui_len), OSP_OK);

	/* Queue UI into server_rx */
	assert_int_equal(mock_send_to_peer(&pair.server_rx, ui_enc, ui_len), OSP_OK);

	/* Verify UI frame is decodable but session remains in IDLE.
	 * UI frames are not valid in NDM state. */
	osp_hdlc_frame_t decoded;
	assert_int_equal(osp_hdlc_deframe(ui_enc, ui_len, &decoded), OSP_OK);
	assert_int_equal(decoded.control.type, OSP_HDLC_TYPE_UI);

	/* Session should still be in IDLE — UI is rejected */
	assert_int_equal(server.state, OSP_HDLC_STATE_IDLE);
}

