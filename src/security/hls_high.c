static uint8_t hls_high_galois_multiply(uint8_t value) {
	if (value >> 7) {
		/* Keep identical truncation behavior to GXCipher::GaloisMultiply:
		 * value is uint8_t (unsigned char), so (value<<1) is truncated to 8 bits
		 * before XOR with 0x1b. */
		value = (uint8_t)(value << 1);
		return (uint8_t)(value ^ 0x1b);
	}
	return (uint8_t)(value << 1);
}

static void hls_high_aes1_encrypt_block(uint8_t data[16], uint8_t key[16]) {
	for (int round = 0; round < 10; ++round) {
		for (int i = 0; i < 16; ++i) {
			data[i] = HLS_HIGH_S_BOX[(data[i] ^ key[i]) & 0xff];
		}

		uint8_t buf1 = data[1];
		data[1] = data[5];
		data[5] = data[9];
		data[9] = data[13];
		data[13] = buf1;

		buf1 = data[2];
		uint8_t buf2 = data[6];
		data[2] = data[10];
		data[6] = data[14];
		data[10] = buf1;
		data[14] = buf2;

		buf1 = data[15];
		data[15] = data[11];
		data[11] = data[7];
		data[7] = data[3];
		data[3] = buf1;

		if (round < 9) {
			for (int i = 0; i < 4; i++) {
				uint8_t buf4 = (uint8_t)(i << 2);
				uint8_t mix = (uint8_t)(data[buf4] ^ data[buf4 + 1] ^ data[buf4 + 2] ^ data[buf4 + 3]);
				buf2 = data[buf4];
				buf1 = (uint8_t)(data[buf4] ^ data[buf4 + 1]);
				buf1 = hls_high_galois_multiply(buf1);
				data[buf4] = (uint8_t)(data[buf4] ^ buf1 ^ mix);
				buf1 = (uint8_t)(data[buf4 + 1] ^ data[buf4 + 2]);
				buf1 = hls_high_galois_multiply(buf1);
				data[buf4 + 1] = (uint8_t)(data[buf4 + 1] ^ buf1 ^ mix);
				buf1 = (uint8_t)(data[buf4 + 2] ^ data[buf4 + 3]);
				buf1 = hls_high_galois_multiply(buf1);
				data[buf4 + 2] = (uint8_t)(data[buf4 + 2] ^ buf1 ^ mix);
				buf1 = (uint8_t)(data[buf4 + 3] ^ buf2);
				buf1 = hls_high_galois_multiply(buf1);
				data[buf4 + 3] = (uint8_t)(data[buf4 + 3] ^ buf1 ^ mix);
			}
		}

		key[0] = (uint8_t)(HLS_HIGH_S_BOX[key[13] & 0xff] ^ key[0] ^ HLS_HIGH_R_CON[round]);
		key[1] = (uint8_t)(HLS_HIGH_S_BOX[key[14] & 0xff] ^ key[1]);
		key[2] = (uint8_t)(HLS_HIGH_S_BOX[key[15] & 0xff] ^ key[2]);
		key[3] = (uint8_t)(HLS_HIGH_S_BOX[key[12] & 0xff] ^ key[3]);
		for (int i = 4; i < 16; i++) {
			key[i] = (uint8_t)(key[i] ^ key[i - 4]);
		}
	}

	for (int i = 0; i < 16; i++) {
		data[i] = (uint8_t)(data[i] ^ key[i]);
	}
}

int osp_hls_high_secure(const uint8_t *data, uint32_t data_len, const uint8_t *secret, uint32_t secret_len, uint8_t *out,
                        uint32_t *out_len) {
	if (!data || !secret || !out || !out_len || secret_len == 0 || data_len == 0) {
		return -1;
	}

	uint32_t len = data_len;
	if (len % 16 != 0) {
		len += 16 - (data_len % 16);
	}
	if (secret_len > data_len) {
		len = secret_len;
		if (len % 16 != 0) {
			len += 16 - (secret_len % 16);
		}
	}
	if (len > OSP_SEC_CHALLENGE_MAX) {
		return -1;
	}

	uint8_t key[16];
	memset(key, 0, sizeof(key));
	memcpy(key, secret, secret_len < 16 ? secret_len : 16);
	if (secret_len < len) {
		/* Gurux pads secret with zeros up to block length */
	}

	memcpy(out, data, data_len);
	if (data_len < len) {
		memset(out + data_len, 0, len - data_len);
	}

	for (uint32_t pos = 0; pos < len; pos += 16) {
		uint8_t round_key[16];
		memcpy(round_key, key, sizeof(round_key));
		hls_high_aes1_encrypt_block(&out[pos], round_key);
	}

	*out_len = len;
	return 0;
}
